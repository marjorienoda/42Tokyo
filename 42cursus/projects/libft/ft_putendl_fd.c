#include "libft.h"
#include <unistd.h>

void ft_putendl_fd(char *s, int fd)
{
    int i;
    i = 0;

    while(s[i] != '\0')
    {
        ft_putchar_fd(s[i], fd);
        i++;
    }
    write(fd, "\n", 1);
}